import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const apiDir = path.resolve(__dirname, '..');

function parseResx(filePath) {
  const xml = fs.readFileSync(filePath, 'utf8');
  const map = {};
  const regex = /<data\s+name="([^"]+)"[^>]*>[\s\S]*?<value>([\s\S]*?)<\/value>/g;
  let match;
  while ((match = regex.exec(xml)) !== null) {
    let value = match[2].trim();
    value = value.replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>').replace(/&quot;/g, '"').replace(/&#39;/g, "'");
    map[match[1]] = value;
  }
  return map;
}

function toPascalCase(snake) {
  return snake.split('_').map(s => s.charAt(0).toUpperCase() + s.slice(1).toLowerCase()).join('');
}

// Discover all resx files and their cultures
const resxFiles = fs.readdirSync(apiDir).filter(f => /^SharedResources(\.\w+)?\.resx$/.test(f));
const cultures = [];
const translations = {};

for (const f of resxFiles) {
  const m = f.match(/^SharedResources(?:\.(\w+))?\.resx$/);
  const culture = m[1] || 'en';
  cultures.push(culture);
  translations[culture] = parseResx(path.join(apiDir, f));
}

// 'en' must be first (default)
cultures.sort((a, b) => a === 'en' ? -1 : b === 'en' ? 1 : 0);

const en = translations['en'] || {};

const statusMap = {};
for (const key of Object.keys(en)) {
  if (key.startsWith('status_')) {
    statusMap[toPascalCase(key.slice(7))] = key;
  }
}

const i18nDir = path.join(apiDir, 'wwwroot', 'i18n');
const jsDir = path.join(apiDir, 'wwwroot', 'js');

fs.mkdirSync(i18nDir, { recursive: true });

for (const culture of cultures) {
  fs.writeFileSync(path.join(i18nDir, culture + '.json'), JSON.stringify(translations[culture], null, 2));
}

const dataJs = `// Generated by build/generate-i18n.mjs — do not edit manually
window.__I18N_DATA = ${JSON.stringify(en, null, 2)};
window.__STATUS_MAP = ${JSON.stringify(statusMap, null, 2)};
window.__I18N_LANGS = ${JSON.stringify(cultures)};
`;

fs.writeFileSync(path.join(jsDir, 'i18n-data.js'), dataJs);

// Guard: check all HTML files loading i18n.js have i18n-data.js before it
const wwwroot = path.join(apiDir, 'wwwroot');
let errors = 0;
function walkDir(dir) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory() && entry.name !== 'i18n') {
      walkDir(full);
    } else if (entry.name.endsWith('.html')) {
      const html = fs.readFileSync(full, 'utf8');
      if (html.includes('/js/i18n.js')) {
        const beforeI18n = html.slice(0, html.indexOf('/js/i18n.js'));
        if (!beforeI18n.includes('i18n-data.js')) {
          console.error('WARNING: ' + path.relative(apiDir, full) + ' loads i18n.js without preceding i18n-data.js');
          errors++;
        }
      }
    }
  }
}
walkDir(wwwroot);
if (errors > 0) {
  console.error('Found ' + errors + ' HTML file(s) missing i18n-data.js reference');
  process.exit(1);
}

const summary = cultures.map(c => c + '.json (' + Object.keys(translations[c]).length + ')').join(', ');
console.log('OK — generated ' + summary + ', statusMap (' + Object.keys(statusMap).length + '), langs ' + JSON.stringify(cultures));
