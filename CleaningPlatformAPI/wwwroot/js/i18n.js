  var _saved = {};

  function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.cookie = 'lang=' + lang + ';path=/';
    applyTranslations();
  }

  function applyTranslations() {
    if (embedded[currentLang]) {
      translations = embedded[currentLang];
      ready = true;
      document.documentElement.lang = currentLang;
      processDOM();
    } else {
      translations = {};
      ready = false;
      document.documentElement.lang = currentLang;
      restoreDOM();
    }
    window.dispatchEvent(new CustomEvent('i18nReady'));
  }

  function __(key) {
    if (!key) return '';
    if (!ready) return null;
    return translations[key] || key;
  }

  function __status(s) {
    if (!s) return '';
    var key = 'status_' + s.replace(/([A-Z])/g, '_$1').toLowerCase().replace(/^_/, '');
    return __(key) || s;
  }

  function processDOM() {
    var els = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < els.length; i++) {
      var el = els[i];
      var key = el.getAttribute('data-i18n');
      var attr = el.getAttribute('data-i18n-attr');
      if (attr) {
        var orig = el.getAttribute(attr);
        if (!_saved[key + '@' + attr]) _saved[key + '@' + attr] = orig;
        el.setAttribute(attr, __(key));
      } else {
        if (!_saved[key]) _saved[key] = el.innerHTML;
        el.innerHTML = __(key);
      }
    }
  }

  function restoreDOM() {
    var els = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < els.length; i++) {
      var el = els[i];
      var key = el.getAttribute('data-i18n');
      var attr = el.getAttribute('data-i18n-attr');
      if (attr) {
        var saved = _saved[key + '@' + attr];
        if (saved !== undefined) el.setAttribute(attr, saved);
      } else {
        if (_saved[key] !== undefined) el.innerHTML = _saved[key];
      }
    }
  }

  function initLanguageSwitcher() {
    var switcher = document.getElementById('lang-switcher');
    if (!switcher) return;
    var btns = switcher.querySelectorAll('.lang-btn');
    for (var i = 0; i < btns.length; i++) {
      btns[i].addEventListener('click', function () {
        var lang = this.getAttribute('data-lang');
        for (var j = 0; j < btns.length; j++) btns[j].classList.remove('active');
        this.classList.add('active');
        setLanguage(lang);
      });
    }
    for (var i = 0; i < btns.length; i++) {
      if (btns[i].getAttribute('data-lang') === currentLang) {
        btns[i].classList.add('active');
      }
    }
  }

  var browserLang = (navigator.language || navigator.userLanguage || '').substring(0, 2);
  if (['hr'].indexOf(browserLang) >= 0) {
    var stored = localStorage.getItem('lang');
    if (!stored) currentLang = browserLang;
  }

  document.addEventListener('DOMContentLoaded', function () {
    initLanguageSwitcher();
    applyTranslations();
  });

  window.__ = __;
